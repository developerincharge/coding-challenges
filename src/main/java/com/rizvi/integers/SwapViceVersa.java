package com.rizvi.integers;

public class SwapViceVersa {

    private static int swap(int n){
        return 7 + 11 - n;
    }

    public static void main(String[] args) {
        System.out.println("Input 7 <-::-> output " +swap(7));
        System.out.println("Input 11 <-::-> output " +swap(11));
    }
}
